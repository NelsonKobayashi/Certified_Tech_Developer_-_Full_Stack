/* Criando uma função */

function calculadora (parametro1, parametro2) {
    return parametro1 + parametro2;
}
console.log(calculadora(1,2));


/* com Arrow Functions */
let calculadora2 = (parametro3, parametro4) =>  parametro3 + parametro4;
console.log(calculadora2(2,3));

